import SwiftUI

extension Font {
    static func holtwood() -> Font {
        return Font.custom("HoltwoodOneSC-Regular", size: 30)
    }
    
    static func holtwood2() -> Font {
        return Font.custom("HoltwoodONeSC-Regular", size: 20)
    }
}
